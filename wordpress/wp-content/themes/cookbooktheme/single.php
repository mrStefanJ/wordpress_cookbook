            <p>This recipe is inspired by a recipe in Liana Krissoff’s book Vegetarian For a New Generation. 
                Don’t let the list of ingredients below intimidate you, it comes together rather quickly 
                and you can repurpose elements throughout meals for the rest of the week. 
                I found myself sprinkling the toasted nuts on just about everything.</p> 
            <p class="text-bold">INGREDIENTS</p>
            <p class="text-bold">For the Matcha Ginger Dressing</p>
            <ul class="sign-not">
                <li>1/4 teaspoon matcha powder</li>
                <li>1" ginger (use more or less according to your taste)</li>
                <li>3 tablespoons olive oil (or other ail)</li>
                <li>2 tablespoons water</li>
                <li>1 tablespoon miso paste</li>
                <li>1 tablespoon tamari</li>
                <li>1/2 teaspoon sesame oil</li>
            </ul><br>
            <p class="text-bold">For the Fried Nut Mixture</p>
            <ul class="sign-not">
                <li>2 tablespoons vegetable oil</li>
                <li>1/2 cup raw cashews</li>
                <li>1/2 cup raw pistachios</li>
                <li>3 cloves garlic, minced</li>
                <li>2 tablespoons sesame seeds</li>
                <li>Salt</li>
            </ul><br>       
            <p class="text-bold">For the Salad</p>
            <ul class="sign-not">
                <li>1 cup brown jasmine rice</li>
                <li>1 can chickpeas, drained and rinsed</li>
                <li>2 carrots, grated</li>
                <li>1 celeriac root, finely grated</li>
                <li>1 pepper, sliced</li>
                <li>Handful tomatoes (optional – they were at the farmer’s market late into fall this year but aren’t usually 
                around come fall/winter)</li>
                <li>Cilantro, to garnish</li>
                <li>Mint, to garnish</li>
            </ul><br>    
            <p class="text-bold">METHOD</p>
            <p class="text-bold">Make the Rice and Chickpeas</p>
            <ul class="sign">
                <li>In a medium pot, combine 1 cup rice, 2 1/4 cups water and bring to a boil. 
                    Stir once, cover, and reduce heat. Simmer for 45 minutes. 
                    Remove from heat and let sit for 5 – 10 minutes before serving.</li>
                <li>Rinse and drain the chickpeas, pat dry with a paper towel. 
                    Coat lightly with olive oil, salt, and spices (like paprika and a pinch of cayenne) 
                    and place in a 425 F oven for about 30 minutes (I usually just take them out when I am ready to serve).</li>
            </ul><br>
            <p class="text-bold">Make the Fried Nut Mixture</p>
            <ul class="sign">
                <li>Line a bowl with a paper towel. 
                    In a large saute pan, heat the oil over medium heat until it shimmer. 
                    Add the nuts, garlic, and a pinch of salt. </li>
                <li>Cook, stirring constantly, until the nuts are turning golden, about 3 – 5 minutes. 
                    Be careful not to burn the garlic. 
                    Transfer to the paper towel-lined bowl to drain. Season with a touch more salt if needed.</li>
            </ul><br>
            <p class="text-bold">Make the Matcha Ginger Dressing</p>
            <ul class="sign">
                <li>Combine all the ingredients in a mini food processor and blend. 
                    Alternately, you can whisk the ingredients together and finely grate the ginger.</li>
            </ul><br>
            <p class="text-bold">Serve</p>
            <ul class="sign">
                <li>Divide the rice among 3 to 4 bowls. Top with chickpeas, carrots, celeriac root, tomatoes, fresh herbs, and fried nut mixture.
                    Drizzle dressing on top, and serve with additional dressing on the side.</li>
                <li>Enjoy!</li>
            </ul>
